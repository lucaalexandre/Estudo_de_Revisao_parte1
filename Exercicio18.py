dia = int(input("Para descobrir o dia da semana digite um numero de 1 a 7: "))
if dia == 1:
    print("O dia correspondente a esse número é Domingo")
elif dia == 2:
    print("O dia correspondente a esse número é Segunda-feira")
elif dia == 3:
    print("O dia correspondente a esse número é Terça-feira")
elif dia == 4:
    print("O dia correspondente a esse número é Quarta-feira")
elif dia == 5:
    print("O dia correspondente a esse número é Quinta-feira")
elif dia == 6:
    print("O dia correspondente a esse número é Sexta-feira")
elif dia == 7:
    print("O dia correspondente a esse número é Sábado")